import React from "react";

// ─────────────────────────────────────────────────────────────
// Lightweight i18n for ATHLOS.
//
// Slovenian is the source language: components call t("…") with the
// Slovenian string and get the English translation when profile.lang
// is "en". Unknown keys fall back to the original, so a missing
// translation can never break the UI.
//
// The language lives on the profile (profile.lang) and is therefore
// saved to the backend and restored on every login/device.
// ─────────────────────────────────────────────────────────────

export const LangContext = React.createContext("sl");
export const useLang = () => React.useContext(LangContext);

export function makeT(lang) {
  if (lang !== "en") return (s) => s;
  return (s) => EN[s] ?? s;
}

export function useT() {
  return makeT(useLang());
}

